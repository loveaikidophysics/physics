def main():
    data = json.loads(DATA_PATH.read_text(encoding="utf-8-sig"))
    changed = 0
    for analysis in data["analysis"]:
        year = int(analysis["year"])
        for question in analysis.get("questions", []):
            key = (year, int(question["no"]))
            solution = question.get("solution", "")
            for old, new in REPLACES.get(key, {}).items():
                if old in solution:
                    solution = solution.replace(old, new)
                    changed += 1
            addition = APPENDS.get(key)
            if addition and addition not in solution:
                solution = solution.rstrip("。") + "。" + addition
                changed += 1
            question["solution"] = solution
    DATA_PATH.write_text(
        "\ufeff" + json.dumps(data, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(f"fixed {changed} remaining audit flags")


if __name__ == "__main__":
    main()
