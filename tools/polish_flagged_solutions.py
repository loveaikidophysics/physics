def main():
    data = json.loads(DATA_PATH.read_text(encoding="utf-8-sig"))
    by_year = {int(a["year"]): a for a in data["analysis"]}
    for (year, no), extra in EXTRA.items():
        questions = by_year[year]["questions"]
        q = next(item for item in questions if int(item["no"]) == no)
        if extra not in q.get("solution", ""):
            q["solution"] = q.get("solution", "").rstrip() + " " + extra
    DATA_PATH.write_text("\ufeff" + json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")


if __name__ == "__main__":
    main()
