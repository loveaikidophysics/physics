def recalc_stats(analysis):
    questions = analysis.get("questions", [])
    total = len(questions) or 1
    unit_counter = Counter(q.get("unit", "未分類") for q in questions)
    analysis["unitStats"] = [
        {"unit": unit, "count": unit_counter[unit], "percent": round(unit_counter[unit] * 100 / total, 1)}
        for unit in sorted(unit_counter, key=lambda unit: UNIT_ORDER.index(unit) if unit in UNIT_ORDER else 99)
    ]

    literacy_counter = Counter()
    for question in questions:
        for name in question.get("literacyTypes", []) or []:
            literacy_counter[name] += 1
    analysis["literacyTypes"] = [{"name": name, "count": literacy_counter.get(name, 0)} for name in LITERACY_NAMES]
    inquiry = literacy_counter.get("實驗與素養探究", 0)
    analysis["inquiryQuestionCount"] = inquiry
    analysis["inquiryQuestionPercent"] = round(inquiry * 100 / total, 1)


def main():
    data = json.loads(DATA_PATH.read_text(encoding="utf-8-sig"))
    changed = 0
    for analysis in data["analysis"]:
        year = int(analysis["year"])
        for question in analysis.get("questions", []):
            key = (year, int(question["no"]))
            if key in SOLUTIONS:
                question["solution"] = SOLUTIONS[key]
                question["solutionStatus"] = "teacher-reviewed-draft"
                question["quality"] = "reviewed-draft"
                question.pop("commonMistake", None)
                question.pop("commonMistakes", None)
                question.pop("keyConcept", None)
                question.pop("keyConcepts", None)
                if key in DIAGRAMS:
                    question["solutionDiagrams"] = [
                        {"title": title, "text": text} for title, text in DIAGRAMS[key]
                    ]
                changed += 1
            if key in UNITS:
                question["unit"] = UNITS[key]
        recalc_stats(analysis)

    DATA_PATH.write_text("\ufeff" + json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"replaced {changed} non-choice solutions")


if __name__ == "__main__":
    main()
